import math

# Metodo para redondear hacia abajo.
resultado = math.floor(12.33333)
print(resultado)

# Metodo para redondear hacia arriba.
resultado2 = math.ceil(12.3333)
print(resultado2)

resultado3 = math.pi
print(resultado3)

# Nos muestra por cuanto deberiamos exponer una base para llegar a x numero
